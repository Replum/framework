namespace Replum\Html;

use \Replum\PageInterface;

/**
 * The pre element represents a block of preformatted text, in which structure is represented by typographic conventions rather than by elements.
 *
 * @author Dennis Birkholz <dennis@birkholz.org>
 * @link https://www.w3.org/TR/html5/grouping-content.html#the-pre-element
 */
final class Pre extends HtmlElement implements FlowElementInterface
{
    const TAG = 'pre';

    public static function create(PageInterface $page, string $text = null) : self
    {
        $element = new self($page);
        if ($text !== null) {
            $element->add(Text::create($page, $text));
        }
        return $element;
    }
}
