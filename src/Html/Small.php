namespace Replum\Html;

use \Replum\PageInterface;

/**
 * The small element represents side comments such as small print.
 *
 * @author Dennis Birkholz <dennis@birkholz.org>
 * @link https://www.w3.org/TR/html5/text-level-semantics.html#the-small-element
 */
final class Small extends HtmlElement implements PhrasingElementInterface
{
    const TAG = 'small';

    public static function create(PageInterface $page, string $text = null) : self
    {
        $element = new self($page);
        if ($text !== null) {
            $element->add(Text::create($page, $text));
        }
        return $element;
    }
}
